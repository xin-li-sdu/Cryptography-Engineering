# 计算每个数的汉明重量（二进制表示中1的个数）
HW = [bin(n).count("1") for n in range(0, 256)]

# 计算数组的均值
def mean(X):
    return np.mean(X, axis=0)

# 计算数组的标准差
def std_dev(X, X_bar):
    return np.sqrt(np.sum((X - X_bar) ** 2, axis=0))

# 计算数组的协方差
def cov(X, X_bar, Y, Y_bar):
    return np.sum((X - X_bar) * (Y - Y_bar), axis=0)

# 将曲线顶点对应的子字节通过相关性分析还原出密钥
def CPA(trace, textin_array):
    # 计算曲线顶点的均值和标准差
    trace_mean = np.average(trace, axis=0)
    trace_omega = np.sqrt(np.sum((trace - trace_mean) ** 2, axis=0))
    cpa_correlation = [0] * 16
    bestguess = [0] * 16
    for byte in trange(0, 16):
        maxcpa = [0] * 256
        # 遍历所有密钥猜测
        for kguess in range(0, 256):
            # 根据密钥猜测计算每个输入字节的汉明重量
            hws = np.array([[HW[aes_internal(textin[byte], kguess)] for textin in textin_array]]).transpose()
            hws_bar = mean(hws)
            o_hws = std_dev(hws, hws_bar)
            # 计算曲线顶点与输入字节之间的协方差
            correlation = cov(trace, trace_mean, hws, hws_bar)
            # 计算CPA相关性
            cpaoutput = correlation / (o_hws * trace_omega)
            maxcpa[kguess] = max(abs(cpaoutput))

        # 找到最大相关性对应的密钥猜测值
        bestguess[byte] = np.argmax(maxcpa)
        cpa_correlation[byte] = max(maxcpa)

    print("Best Key Guess: ", end="")
    for b in bestguess:
        print("%02x " % b, end="")
    print("\n", cpa_correlation)


